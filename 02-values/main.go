package main

import "fmt"

func main() {
	fmt.Println("Hello" + " World") // Stringi mogą być łączone poprzez znak +
	fmt.Println("1+5=", 1+5) // Liczby całkowite (integer) i liczby zmiennoprzecinkowe (float)
	fmt.Println(true && false) // Typ Prawda / Fałsz za pomocą operatorów logicznych
	fmt.Println(true || false) 
}